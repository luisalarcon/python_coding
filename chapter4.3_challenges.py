def addFive(a, b, c, d=0, e=0):
    """
    Returns addition of all variables.
    :param a: int.
    :param b: int.
    :param c: int.
    :param d: int.
    :param e: int.
    :return: ins sum of a,b,c,d and e
    """
    return a+b+c+d+e

print(addFive(1,1,1))
print(addFive(1,1,1,1))
